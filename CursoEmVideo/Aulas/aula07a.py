n1 = int(input('Um valor: '))
n2 = int(input('Outro valor: '))
print(f'A soma vale {n1 + n2}.', end='') # end Não deixa quebra a linha de um print para o outro.
print(f'A multiplicação vale {n1 * n2}.')
print(f'A divisão vale {n1 / n2}.')
print(f'A divisão inteira vale {n1 // n2}.')
print(f'O resto vale {n1 % n2}.')
print(f'A potencia vale \n {n1 ** n2}.') # \n usando para quebrar a linha.
